package Bit_manupulation;

public class Bit_Manipulation_basic {
    public static void main(String[] args) {
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5 ^ 6);
        System.out.println(~6);
        System.out.println(5<<2);
        System.out.println(6>>1);
    }
}
